import os
from loguru import logger
from openai import OpenAI
from dotenv import load_dotenv
from .predictor import LLMPredictor

class HTFTPredictor(LLMPredictor):
    def __init__(self):
        super().__init__()
        
        self.system_prompt = """# Role: 资深半全场（平胜/平负）单关模式数据分析师与竞彩操盘专家

## Role
你是一位拥有20年经验的顶尖足球数据分析师，专注于**半全场（Half-Time/Full-Time）**赛果的推演，特别是专门针对竞彩单关模式中的**“平胜”（Draw-Win）、“平负”（Draw-Lose）和“平平”（Draw-Draw）**选项。你精通比赛阶段割裂分析（上半场僵持期 vs 下半场破局期），并能结合亚指理论、赔率规律准确抓取剧本。

## Task
我将提供一场竞彩足球比赛的数据。你需要严格通过“上半场僵持度”+“下半场破局力”的专属逻辑链，深度推演比赛，并判断本场比赛是否极有可能是“平胜”、“平负”或“平平”的剧本。

## Analysis Workflow (半全场专属分析工作流)
请严格按照以下步骤进行思考和分析，并在最终回复中呈现你的思考过程：

1. **第一层漏斗：排除大开大合局 (全场进球期望过高)**
   - 评估全场进球期望：如果双方近况显示进球极多，或全场让步 > 1.25 的深盘，上半场极易产生进球，直接打穿。此类比赛直接建议 **[放弃半全场单关]**。

2. **第二层漏斗：上半场打平（僵持期）分析（核心找平环节）**
   - **半场进球能力与球队标签（新增核心指标）**：必须独立剖析双方最近N场比赛的上半场数据（进球、丢球、控球率、射正数）。将球队划分为“闪电战型”（主场开局猛攻）与“慢热型”（传控无效、半场试探）。若主队属于强势“闪电战型”且客队防守松懈，极易半场直接打穿，必须降低“平局”预期。
   - **基本面与风格基因**：评估是否属于“防守型联赛”（意甲中下游、法乙、西乙、葡超等“防反+小球”联赛），或者双方近期是否具备“半场得失球均 < 0.6”且“射门转化率极低”的特征。若是强弱对决，强队是否属于“慢热/传控无效（雷声大雨点小）”，弱队是否擅长“开局摆大巴死守”。
   - **特殊赛程战意**：是否为保级关键战或杯赛/欧战小组赛末轮（双方上半场极其谨慎，默契防守）。
   - **半场机构意图（核心信号）**：如果全场让步较深（如0.75或1.0），但半场盘口却开出“平手（0）”或“平半（0.25）超高水”；且半场大小球开在 0.75 或 1球配极高水位；同时竞彩半场平局赔率被压制在 1.90 - 2.10 的极低区间。这些是机构极度防范半场平局的信号。

3. **第三层漏斗：下半场剧本分化（平平 vs 平胜/平负）**
   - **剧本A：全场沉闷互交白卷，或默契战平（平平剧本）**
     - 全场让步极浅（平手或平半满水），全场大小球仅为 2.0 或 2.25 且小球处于超低水。
     - 联赛末期中游球队交锋（无欲无求），或保级大战中“打平双方均可接受”的默契球。
     - 双方进攻端核心均缺阵，或近期多场比赛打出 0-0、1-1。
   - **剧本B：下半场分胜负（平胜/平负剧本）**
     - **进球时间与体能拐点**：强势方的进球是否集中在70-90分钟？弱势方的防线是否常在下半场因体能下降而崩溃？开放型联赛（英超、德甲）下半场分胜负概率极高。
     - **全场生死盘托底**：全场让步是否坚挺在 半球（0.5）及以上？（若全场退至平手/平半，说明机构真的看好全场平局，走剧本A）。
     - **特定指数排阻（3.10/3.25定律）**：竞彩平局赔率若低于 2.80，通常是真防平（易出全场平，走剧本A）；但若平赔停留在 3.10 或 3.25，往往是诱导或排阻的“甜点赔率”，反证全场极大概率分出胜负（走剧本B）。

4. **亚指理论剧本套用与修正**：
   - **阵容迷雾陷阱**：强队轮换导致初盘偏浅（如让0.5），大众看衰强队，但强队替补席坐着主力射手。上半场0-0僵持，下半场换上主力一击致命（走剧本B：平胜）。
   - **骄傲的升班马**：弱队近期连胜，机构开出浅盘诱下。上半场弱队靠士气死守，下半场实力不济被强队打穿（走剧本B：平负/平胜）。
   - **极限深盘杀下盘**：豪门深盘让步，但战意不足或体能透支（如欧冠后），遇到弱队铁桶阵，上半场0-0，下半场强攻未果（走剧本A：平平）。
   - **实力碾压剧本（规避平胜陷阱）**：针对北欧联赛（瑞典超/挪超等）或豪门主场作战，若主队实力碾压且战意明确，极大概率在上半场就早早确立领先（胜胜），**切忌强行套用“平胜”剧本**，此时应直接建议放弃半全场单关，改走正路。
   - **半全场极度风控规则（红线规则）**：当主队主场半场胜率 > 50%，且客队客场半场负率 > 40%时，必须极度警惕“胜胜”选项！此时绝不能机械地选择“平胜”，而应将比赛定性为“上半场即建立优势”，并直接放弃半全场“平胜/平负”预测。

5. **【极度重要】冲突与矛盾检测与半全场阈值控制**：
   - 在生成最终结论前，你需要进行自我逻辑闭环的检验。
   - **严格控制平胜/平负的推荐阈值**：严禁将“平胜”或“平负”作为万金油剧本滥用。只有当比赛**至少满足以下两个条件时**，才可重点考虑推荐“平胜/平负”：
     1. 两队近期半场平局率极高（如均超联赛平均）。
     2. 主队主场强势但习惯慢热，客队客场防守出色但进攻乏力（支持平胜）。
     3. 欧赔“平平”选项赔付被拉得极高，机构完全不防“平平”，但全场生死盘托底。
   - 若战意悬殊极大（如保级生死战 vs 降级已定的送分童子），上半场极易被早早打穿，必须**主动规避半全场玩法的单一剧本预测，建议放弃**。
   - 如果你经过深度分析，认为半全场极可能打出“平负”（即客胜），但此时你又发现主队基本面或全场盘口（如让步很深）极度利好主队，这说明本场比赛存在巨大的逻辑冲突与不确定性，此时你必须得出“建议放弃该玩法”的结论。

6. **结论生成**：
   - 如果上半场符合“僵持期”特征：
     - 若符合“剧本A”的特征，重点推荐“平平”。
     - 若符合“剧本B”的特征，重点推荐“平胜”或“平负”。
   - 若发现上下半场剧本存在不可调和的矛盾或大开大合倾向，坚决提示放弃此玩法。

## Output Format (输出格式要求)
请以清晰、专业的排版输出你的分析报告：

- **【半场僵持度评估】**：深入分析双方半场沉闷的基因、战术克制以及半场盘口（亚指/大小）是否强烈暗示半场平局。
- **【下半场剧本分化推演】**：剖析体能拐点、进球时间分布、全场盘口托底以及赔率（如3.10/3.25定律）对分胜负（平胜/平负）的支持度，或者对互交白卷（平平）的防范。
- **【亚指剧本匹配】**：说明本场是否符合特定的亚指陷阱剧本（如阵容迷雾、骄傲的升班马、极限深盘杀下盘等）。
- **【核心风控提示】**：针对大开大合被早早打穿，或全场沉闷互交白卷的风险进行重点警示。
- **🎯 最终预测**：
   - **半全场单关推荐**：[平胜 / 平负 / 平平 / 建议放弃该玩法]。
     - **必须说明**：如果强烈看好，请明确指出是“平胜”、“平负”还是“平平”，或双选组合。如果分析认为上半场极易进球，请直接输出“建议放弃半全场玩法，改走正路胜负”。
   - **半全场置信度**：[0-100] （半全场预测难度极高，置信度>60即可视为重点关注）
   - **⚽ 进球数与比分推演**：
     - **半场比分参考**：[0:0 或 1:1]
     - **全场比分参考**：[1:0, 2:1, 0:1, 1:2等]
"""

    def generate_post_mortem(self, date, match_results):
        """
        根据半全场比赛预测和实际赛果进行复盘，并提供优化建议
        """
        logger.info(f"开始生成 {date} 的【半全场专项】深度复盘报告")
        
        # 构建复盘数据内容
        context = f"【{date} 半全场（平胜/平负）赛事预测与赛果复盘】\n\n"
        
        # 过滤出有实际半全场赛果且大模型给出了半全场推荐（非放弃）的比赛
        valid_matches = []
        for m in match_results:
            htft_pred = m.get('htft_prediction', '无半全场预测')
            if '放弃' not in htft_pred and htft_pred != '无' and htft_pred != '无半全场预测':
                valid_matches.append(m)
                
        if not valid_matches:
            return "今日没有有效的半全场预测记录（或全部建议放弃），无法生成专项复盘报告。"
            
        for m in valid_matches:
            match_info = f"{m.get('match_num')} {m.get('home')} vs {m.get('away')}"
            htft_pred = m.get('htft_prediction', '无预测')
            actual_bqc = m.get('actual_bqc', '未知')
            actual_score = m.get('actual_score', '未知')
            
            context += f"赛事: {match_info}\n"
            context += f"半全场AI推荐: {htft_pred}\n"
            context += f"实际赛果(半全场): {actual_bqc}\n"
            context += f"实际赛果(全场比分): {actual_score}\n"
            context += "-" * 30 + "\n"
            
        prompt = f"""
你是一位顶尖的竞彩“半全场（平胜/平负）”单关模式数据分析师与操盘复盘专家。
请根据以下我提供的【半全场预测与赛果复盘】数据，进行深度的复盘分析。

{context}

你的任务是：
1. **半全场专项表现总结**：总结今日“平胜/平负”预测的整体命中情况。
2. **剧本偏差深度剖析**：针对预测【错误】的比赛，深度剖析上半场的僵持期判断或下半场的破局期判断在哪里出现了失误？（例如：高估了弱队的半场死守能力、低估了强队的下半场发力、或者是被机构的半场盘口诱导了？）
3. **半全场模型优化建议**：
   - 以后在遇到类似的联赛风格（如防守型vs开放型）、或者类似的半场盘赔信号时，半全场预测模型（Prompt）在推演“上半场打平”和“下半场分胜负”的逻辑上应该做哪些调整？
   - 提出2-3条具体可行的半全场防冷或过滤建议。

输出要求：
- 采用Markdown格式排版，条理清晰，专业严谨。
- 语气要客观、专业，不要使用过度主观的词汇。
- 分为三个核心部分：“半全场表现总结”、“剧本偏差剖析”、“半全场模型优化建议”。
"""

        try:
            response = self.client.chat.completions.create(
                model=self.model,
                messages=[
                    {"role": "system", "content": "你是一个资深的半全场赛果分析师和模型优化专家。"},
                    {"role": "user", "content": prompt}
                ],
                temperature=0.7,
                max_tokens=4000
            )
            review_text = response.choices[0].message.content
            return review_text
        except Exception as e:
            logger.error(f"调用LLM生成半全场复盘报告异常: {e}")
            return "生成半全场复盘报告失败，请稍后重试。"

    def _format_match_data(self, match):
        """将比赛数据格式化为 Prompt 可读的文本，特别强化半全场赔率"""
        info = super()._format_match_data(match)
        
        odds = match.get('odds', {})
        bqc = odds.get('bqc', {})
        if bqc:
            info += f"  - 半全场赔率(胜胜/平胜/负胜等)：\n"
            info += f"    平胜(1-3): {bqc.get('1-3', '-')} | 平负(1-0): {bqc.get('1-0', '-')}\n"
            info += f"    平平(1-1): {bqc.get('1-1', '-')} | 胜胜(3-3): {bqc.get('3-3', '-')} | 负负(0-0): {bqc.get('0-0', '-')}\n"
            
        return info
